#!/usr/bin/env python3
"""
A simple Hello World program in Python.

This program demonstrates the basic structure of a Python script
and prints a greeting message to the console.
"""


def main():
    """
    Main function that prints the Hello World message.
    
    This function serves as the entry point for the program
    and contains the core logic.
    """
    print("Hello, World!")


if __name__ == "__main__":
    # This ensures the main function only runs when the script
    # is executed directly, not when imported as a module
    main()